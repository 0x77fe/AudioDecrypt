		if (doc.HasMember("musicName"))
		{
			rapidjson::Value& a = doc["musicName"];
			inf.musicName = a.GetString();
		}

		//后缀名
		if (doc.HasMember("format"))
		{
			rapidjson::Value& a = doc["format"];
			inf.format = a.GetString();
		}

